function escapeHtml(s: string): string {
  return s
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;');
}

const privacyByLang: Record<LegalLang, string> = {
  tr: privacyTR,
  en: privacyEN,
  ar: privacyEN,
  de: privacyEN,
  fr: privacyEN,
  ru: privacyEN,
  es: privacyEN,
};

const termsByLang: Record<LegalLang, string> = {
  tr: termsTR,
  en: termsEN,
  ar: termsEN,
  de: termsEN,
  fr: termsEN,
  ru: termsEN,
  es: termsEN,
};

