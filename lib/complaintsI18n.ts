export function complaintsText(key: keyof typeof UI.tr): string {
  const l = lang();
  return UI[l][key];
}

export function complaintTypeLabel(type: string): string {
  const l = lang();
  return (TYPE_LABELS[l] as Record<string, string>)[type] ?? type;
}

export function complaintStatusLabel(status: string): string {
  const l = lang();
  return (STATUS_LABELS[l] as Record<string, string>)[status] ?? status;
}

export function complaintCategoryLabel(category: string): string {
  const l = lang();
  return (CATEGORY_LABELS[l] as Record<string, string>)[category] ?? category;
}

const LOCALE_BY_LANG: Record<Lang, string> = {
  tr: 'tr-TR',
  en: 'en-GB',
  ar: 'ar-SA',
  de: 'de-DE',
  fr: 'fr-FR',
  ru: 'ru-RU',
  es: 'es-ES',
};

/** BCP-47 tag for `Date#toLocaleString` / `toLocaleDateString` based on app language. */
export function complaintsLocaleTag(): string {
  return LOCALE_BY_LANG[lang()];
}

export function guestComplaintTimelineManagerDesc(name: string): string {
  return complaintsText('timelineManagerReviewingDesc').replace(/\{\{name\}\}/g, name);
}

export function formatGuestComplaintPushBody(status: string, note: string | undefined): string {
  const statusL = complaintStatusLabel(status);
  const base = `${complaintsText('notifStatusPrefix')}${statusL}`;
  const t = (note ?? '').trim();
  if (!t) return base;
  return `${base} · ${complaintsText('notifNotePrefix')}${t.slice(0, 80)}`;
}
