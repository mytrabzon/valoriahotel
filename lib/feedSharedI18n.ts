export type FeedSharedTextKey = keyof typeof UI.tr;

export function feedSharedText(key: FeedSharedTextKey, vars?: Record<string, string | number>): string {
  const l = lang();
  let s = (UI[l][key] as string) ?? String(key);
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replace(new RegExp(`\\{\\{${k}\\}\\}`, 'g'), String(v));
    }
  }
  return s;
}

export function getFeedReportReasons(): { value: string; label: string }[] {
  return [
    { value: 'spam', label: feedSharedText('feedReportSpam') },
    { value: 'inappropriate', label: feedSharedText('feedReportInappropriate') },
    { value: 'violence', label: feedSharedText('feedReportViolence') },
    { value: 'hate', label: feedSharedText('feedReportHate') },
    { value: 'false_info', label: feedSharedText('feedReportFalseInfo') },
    { value: 'other', label: feedSharedText('feedReportOther') },
  ];
}

